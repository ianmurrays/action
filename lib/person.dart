class PersonPage extends StatefulWidget {
  const PersonPage({super.key});

  @override
  State<PersonPage> createState() => _PersonPageState();
}

class _PersonPageState extends State<PersonPage> {
  var _showAllBiography = false;

  List<Widget> _personalDetail({required String label, required String value}) {
    return [
      Text(
        label,
        style: Theme.of(context).textTheme.labelLarge?.copyWith(
              // bold
              fontWeight: FontWeight.w700,
            ),
      ),
      Text(
        value,
        style: Theme.of(context).textTheme.labelLarge,
      ),
      const SizedBox(height: 10),
    ];
  }

  String _gender(int gender) {
    switch (gender) {
      case 0:
        return 'Not set / not specified';
      case 1:
        return 'Female';
      case 2:
        return 'Male';
      case 3:
        return 'Non-binary';
      default:
        return 'Unknown';
    }
  }

  @override
  Widget build(BuildContext context) {
    final birthday = personData['birthday'] as String;
    final age = DateTime.now().year - DateTime.parse(birthday).year;

    // is the person dead?
    List<Widget>? deadday;

    if (personData['deathday'] != null) {
      deadday = _personalDetail(
          label: 'Died', value: personData['deathday'] as String);
    }

    return Scaffold(
      extendBodyBehindAppBar: true,
      appBar: BlurredAppBar(
        title: personData['name'] as String,
        actions: [
          IconButton(
            icon: const Icon(Icons.push_pin),
            onPressed: () {},
          ),
          IconButton(
            icon: const Icon(Icons.share),
            onPressed: () {},
          ),
        ],
      ),
      body: SingleChildScrollView(
        padding: const EdgeInsets.only(
          bottom: 40,
          top: 110,
        ),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Padding(
              padding: const EdgeInsets.symmetric(horizontal: 8),
              child: Row(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Poster(
                    imagePath: personData['profile_path'] as String,
                    height: 250,
                    width: 166,
                  ),
                  const SizedBox(width: 10),
                  Expanded(
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        ..._personalDetail(
                            label: 'Known For',
                            value:
                                personData['known_for_department'] as String),
                        ..._personalDetail(
                            label: 'Gender',
                            value: _gender(personData['gender'] as int)),
                        ..._personalDetail(
                            label: 'Birthday',
                            value:
                                "${personData['birthday']} ($age years old)"),
                        ..._personalDetail(
                            label: 'Place of Birth',
                            value: personData['place_of_birth'] as String),
                        if (deadday != null) ...deadday,
                      ],
                    ),
                  ),
                ],
              ),
            ),
            const SizedBox(height: 10),
            Padding(
              padding: const EdgeInsets.symmetric(horizontal: 8),
              child: Text(
                'Biography',
                style: Theme.of(context).textTheme.headlineSmall,
              ),
            ),
            const SizedBox(height: 10),
            Padding(
              padding: const EdgeInsets.symmetric(horizontal: 8),
              child: GestureDetector(
                onTap: () {
                  setState(() {
                    _showAllBiography = !_showAllBiography;
                  });
                },
                child: Text(
                  personData['biography'] as String,
                  style: Theme.of(context).textTheme.bodyLarge,
                  maxLines: _showAllBiography ? null : 5,
                  overflow: _showAllBiography ? null : TextOverflow.ellipsis,
                ),
              ),
            ),
            const SizedBox(height: 10),
            Padding(
              padding: const EdgeInsets.symmetric(horizontal: 8),
              child: Text(
                'Movies',
                style: Theme.of(context).textTheme.headlineSmall,
              ),
            ),
            buildCredits(
              (personData['movie_credits'] as Map<String, dynamic>)['cast'],
              titleKey: 'original_title',
              nameKey: 'character',
            ),
            Padding(
              padding: const EdgeInsets.symmetric(horizontal: 8),
              child: Text(
                'TV Shows',
                style: Theme.of(context).textTheme.headlineSmall,
              ),
            ),
            buildCredits(
              (personData['tv_credits'] as Map<String, dynamic>)['cast'],
              titleKey: 'original_name',
              nameKey: 'character',
            ),
            Padding(
              padding: const EdgeInsets.symmetric(horizontal: 8),
              child: Text(
                'Movies (as crew)',
                style: Theme.of(context).textTheme.headlineSmall,
              ),
            ),
            buildCredits(
              (personData['movie_credits'] as Map<String, dynamic>)['crew'],
              titleKey: 'original_title',
              nameKey: 'job',
            ),
            Padding(
              padding: const EdgeInsets.symmetric(horizontal: 8),
              child: Text(
                'TV Shows (as crew)',
                style: Theme.of(context).textTheme.headlineSmall,
              ),
            ),
            buildCredits(
              (personData['tv_credits'] as Map<String, dynamic>)['crew'],
              titleKey: 'original_name',
              nameKey: 'job',
            ),
          ],
        ),
      ),
    );
  }

  Widget buildCredits(List<dynamic> items,
      {required String titleKey, required String nameKey}) {
    return SizedBox(
      height: 280,
      child: ListView.builder(
        padding: const EdgeInsets.only(right: 5, left: 5),
        scrollDirection: Axis.horizontal,
        itemCount: items.length,
        itemBuilder: (context, index) {
          var item = items[index];
          var title = item[nameKey] as String;
          var subtitle = item[titleKey] as String;

          return PosterTile(
            imagePath: item['poster_path'] as String,
            title: title,
            subtitle: subtitle,
            onTap: () {
              Navigator.push(
                context,
                MaterialPageRoute(
                  builder: (context) => const DetailPage(),
                ),
              );
            },
          );
        },
      ),
    );
  }
}
